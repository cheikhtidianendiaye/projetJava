package entities;

public class Menage
{
	private int idM;
	private String nomFamille;
	private Village idV;

	public int getIdM()
	{
		return idM;
	}
	public void setIdM(int idM)
	{
		this.idM = idM;
	}
	public String getNomFamille()
	{
		return nomFamille;
	}
	public void setNomFamille(String nomFamille)
	{
		this.nomFamille = nomFamille;
	}
	public Village getIdV() {
		return idV;
	}
	public void setIdV(Village idV)
	{
		this.idV = idV;
	}





}
